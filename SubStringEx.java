package string.jagdish.Practice;

public class SubStringEx {
	
	public static void main(String[] args) {
		//part of String means Substring
		
		String str= "Siva Kumar";//substring also counts white spaces
		System.out.println(str.substring(5));//printing starts from K ;K's index starts from 5 o/p=kumar
		System.out.println(str.substring(0,5));//printing starts from 0 and ends upto 5
	}

}
