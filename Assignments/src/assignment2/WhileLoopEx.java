public class WhileLoopEx {
	
	public static void main(String[] args) {
		 int i=1, n=10;
		
		  while(i<=n)
		    {
		        /* Check even condition before printing */
		        if(i%2==0)
		        {
		            System.out.print(i+" ");
		        }

		        i++;
		    }
	}

}
